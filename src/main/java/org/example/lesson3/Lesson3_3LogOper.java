public class Lesson3_3LogOper {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        int c =  ++a - b * 2;
        System.out.println(c);

        int d = 'a'; // это 97 порядковый номер в unicode
        int e = 10;

        System.out.println(d + e); // по этому здесь будет 107

    }

}
